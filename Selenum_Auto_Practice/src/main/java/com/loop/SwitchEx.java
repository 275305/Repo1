package com.loop;

public class SwitchEx {
	
	
	public static void main(String[] args) {
		
		int x='c';
		
		switch(x) {
		
		case 1: System.out.println("case1");
		
		break;
		
		case 'c':System.out.println("case2");
		
		
		default:
		System.out.println("default");
		
		}
	}

}
